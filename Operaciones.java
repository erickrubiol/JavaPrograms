public class Operaciones{
    public static void main(String args[]){
        int numero_uno = 17;
        int numero_dos = 5;
        int resultado = 0;
        
        // Entero
        resultado = numero_uno / numero_dos;
        System.out.println(resultado);
        
        // Sobrante de la division
        resultado = numero_uno % numero_dos;
        System.out.println(resultado);
        
    }
}
